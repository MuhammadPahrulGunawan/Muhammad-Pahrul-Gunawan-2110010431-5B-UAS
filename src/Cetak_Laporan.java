    private void btncariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncariActionPerformed
    // Mengambil tanggal dari komponen jDate1 dan jDate2
    java.util.Date tgl = (java.util.Date) this.jDate1.getDate();
    java.util.Date tgl1 = (java.util.Date) this.jDate2.getDate();
    // Memeriksa apakah kedua tanggal tidak kosong
    if (new java.sql.Date(tgl.getTime()).toString().isEmpty() && new java.sql.Date(tgl1.getTime()).toString().isEmpty()) {
    JOptionPane.showMessageDialog(this, "Tanggal data pencarian masih kosong");
    } else {
    try {
        // Format tanggal ke format yang sesuai dengan query SQL
        SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
        String a = new java.sql.Date(tgl.getTime()).toString();
        String b = new java.sql.Date(tgl1.getTime()).toString();
        // Menentukan kolom yang akan diambil dari tabel
        String kolom[] = { "tgl_peminjaman", "tgl_kembali","peminjam", "nopol", "harga", "lama", "total"};
        // Membuat query SQL dengan menggunakan metode querySelect dari objek con (Database)
        rs = con.querySelect(kolom ,"tb_laporan"," DATE_FORMAT(tgl_peminjaman,'%Y-%m-%d')>='"+a+"' AND DATE_FORMAT(tgl_peminjaman,'%Y-%m-%d')<= '"+b+"' "); 
        // Menetapkan hasil query ke model tabel
        tabel_laporan.setModel(new Database.ResultSetTable(rs));
    } catch (Exception e) {
        // Menampilkan pesan kesalahan jika terjadi exception
        JOptionPane.showMessageDialog(this, "Pencarian Error");
    }
}
    // Membersihkan komponen setelah pencarian selesai
    clear();
    }//GEN-LAST:event_btncariActionPerformed

