    public void cetak() {
        // Membersihkan tampilan sebelum melakukan operasi
        clear();
        // Membuat format tanggal untuk pemilihan rentang tanggal
        SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
        String a = String.valueOf(format.format(jDate1.getDate()));
        String b = String.valueOf(format.format(jDate2.getDate())); 
    try {
    // Mengeksekusi query untuk mengambil total dari tb_laporan dalam rentang tanggal tertentu
    rs = con.eksekusiQuery("SELECT SUM(total) FROM tb_laporan WHERE DATE_FORMAT(tgl_peminjaman, '%Y-%m-%d') >= '" + a + "' AND DATE_FORMAT(tgl_peminjaman, '%Y-%m-%d') <= '" + b + "'");
    
    while(rs.next()){
        // Mendapatkan nilai total dari hasil query
        lap = rs.getString(1);
    }
    try {
        // Mencetak tabel_laporan dengan mode FIT_WIDTH dan menambahkan header dengan total transaksi
        tabel_laporan.print(JTable.PrintMode.FIT_WIDTH, new MessageFormat("Data Transaksi dengan Total : Rp. " + lap), null);
    } catch(PrinterException ep) {
        // Menangani exception jika terjadi kesalahan saat mencetak
        JOptionPane.showMessageDialog(null, ep);
    }
    } catch (SQLException es) {
    // Menangani exception jika terjadi kesalahan SQL
    JOptionPane.showMessageDialog(rootPane, "Error " + es);
    }
    }
